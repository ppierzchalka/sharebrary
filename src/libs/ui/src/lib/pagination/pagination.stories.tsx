import type { Meta, StoryObj } from '@storybook/react';
import {
  Pagination,
  PaginationContent,
  PaginationEllipsis,
  PaginationItem,
  PaginationLink,
  PaginationNext,
  PaginationPrevious,
} from './pagination';

/**
 * The Pagination component provides a way to navigate between pages of content.
 *
 * It consists of several subcomponents that you can compose to create different pagination layouts.
 * - `Pagination`: Container component
 * - `PaginationContent`: Wraps the pagination items
 * - `PaginationItem`: Individual page item container
 * - `PaginationLink`: Link for individual pages
 * - `PaginationPrevious`: Previous page link
 * - `PaginationNext`: Next page link
 * - `PaginationEllipsis`: Indicates skipped page numbers
 */
const meta: Meta<typeof Pagination> = {
  component: Pagination,
  title: 'Base Components/Pagination',
  tags: ['autodocs'],
};

export default meta;
type Story = StoryObj<typeof Pagination>;

/**
 * Basic pagination with page numbers and navigation buttons
 */
export const Default: Story = {
  render: () => (
    <Pagination>
      <PaginationContent>
        <PaginationItem>
          <PaginationPrevious href="#" />
        </PaginationItem>
        <PaginationItem>
          <PaginationLink href="#" isActive>
            1
          </PaginationLink>
        </PaginationItem>
        <PaginationItem>
          <PaginationLink href="#">2</PaginationLink>
        </PaginationItem>
        <PaginationItem>
          <PaginationLink href="#">3</PaginationLink>
        </PaginationItem>
        <PaginationItem>
          <PaginationNext href="#" />
        </PaginationItem>
      </PaginationContent>
    </Pagination>
  ),
};

